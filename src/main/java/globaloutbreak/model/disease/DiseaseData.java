package globaloutbreak.model.disease;

/**
 * Class to save all Disease data.
 */
public class DiseaseData {
    private String type;
    private float infectivity;
    private float lethality;
    private float airInfectivity;
    private float landInfectivity;
    private float seaInfectivity;
    private float heatInfectivity;
    private float coldInfectivity;
    private float cureResistance;
    private float humidityInfectivity;
    private float aridityInfectivity;
    private float povertyInfectivity;

    /**
     * 
     * @return
     *         type
     */
    public String getType() {
        return this.type;
    }

    /**
     * 
     * @return
     *         infectivity
     */
    public Float getInfectivity() {
        return this.infectivity;
    }

    /**
     * 
     * @return
     *         lethality
     */
    public Float getLethality() {
        return this.lethality;
    }

    /**
     * 
     * @return
     *         air infectivity
     */
    public Float getAirInfectivity() {
        return this.airInfectivity;
    }

    /**
     * 
     * @return
     *         land infectivity
     */
    public Float getLandInfectivity() {
        return this.landInfectivity;
    }

    /**
     * 
     * @return
     *         sea infectivity
     */
    public Float getSeaInfectivity() {
        return this.seaInfectivity;
    }

    /**
     * 
     * @return
     *         heat infectivity
     */
    public Float getHeatInfectivity() {
        return this.heatInfectivity;
    }

    /**
     * 
     * @return
     *         cold infectivity
     */
    public Float getColdInfectivity() {
        return this.coldInfectivity;
    }

    /**
     * 
     * @return
     *         cure resistance
     */
    public Float getCureResistance() {
        return this.cureResistance;
    }

    /**
     * 
     * @return
     *         humidity infectivity
     */
    public Float getHumidityInfectivity() {
        return this.humidityInfectivity;
    }

    /**
     * 
     * @return
     *         aridity infectivity
     */
    public Float getAridityInfectivity() {
        return this.aridityInfectivity;
    }

    /**
     * 
     * @return
     *         poverty infectivity
     */
    public Float getPovertyInfectivity() {
        return this.povertyInfectivity;
    }

    /**
     * 
     * @param type
     */
    public void setType(final String type) {
        this.type = type;
    }

    /**
     * 
     * @param infectivity
     */
    public void setInfectivity(final float infectivity) {
        this.infectivity = infectivity;
    }

    /**
     * 
     * @param lethality
     */
    public void setLethality(final float lethality) {
        this.lethality = lethality;
    }

    /**
     * 
     * @param airInfectivity
     */
    public void setAirInfectivity(final float airInfectivity) {
        this.airInfectivity = airInfectivity;
    }

    /**
     * 
     * @param landInfectivity
     */
    public void setLandInfectivity(final float landInfectivity) {
        this.landInfectivity = landInfectivity;
    }

    /**
     * 
     * @param seaInfectivity
     */
    public void setSeaInfectivity(final float seaInfectivity) {
        this.seaInfectivity = seaInfectivity;
    }

    /**
     * 
     * @param heatInfectivity
     */
    public void setHeatInfectivity(final float heatInfectivity) {
        this.heatInfectivity = heatInfectivity;
    }

    /**
     * 
     * @param coldInfectivity
     */
    public void setColdInfectivity(final float coldInfectivity) {
        this.coldInfectivity = coldInfectivity;
    }

    /**
     * 
     * @param cureResistance
     */
    public void setCureResistance(final float cureResistance) {
        this.cureResistance = cureResistance;
    }

    /**
     * 
     * @param humidityInfectivity
     */
    public void setHumidityInfectivity(final float humidityInfectivity) {
        this.humidityInfectivity = humidityInfectivity;
    }

    /**
     * 
     * @param aridityInfectivity
     */
    public void setAridityInfectivity(final float aridityInfectivity) {
        this.aridityInfectivity = aridityInfectivity;
    }

    /**
     * 
     * @param povertyInfectivity
     */
    public void setPovertyInfectivity(final float povertyInfectivity) {
        this.povertyInfectivity = povertyInfectivity;
    }
}
